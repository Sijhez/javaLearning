package moduloTres;

public class OperadoresUnarios {
    public static void main(String[] args) {
        int i = -5;
        int j = +i; // esto es j = (1)*i => -5
        System.out.println("j = " + j);
        
        int k = -i; // esto es k = (-1)*i => 5
        System.out.println("k = " + k);


    }
}
